<?php
// Перевіряємо, чи були передані змінні c і d через POST
if (isset($_POST['c']) && isset($_POST['d'])) {
    // Отримуємо значення змінних c і d з форми і змінюємо їх тип на цілі числа
    $c = (int)$_POST['c'];
    $d = (int)$_POST['d'];

    // Виконуємо математичні операції
    $sum = $c + $d;
    $product = $c * $d;
    $difference = $c - $d;

    // Перевіряємо, чи друге число не є нулем, щоб уникнути ділення на нуль
    if ($d != 0) {
        $quotient = $c / $d;
    } else {
        $quotient = "Ділення на нуль";
    }

    // Виводимо результати
    echo "<h2>Результати математичних операцій:</h2>";
    echo "<p>Сума: $c + $d = $sum</p>";
    echo "<p>Добуток: $c * $d = $product</p>";
    echo "<p>Різниця: $c - $d = $difference</p>";
    echo "<p>Частка: $c / $d = $quotient</p>";
} else {
    // Якщо змінні c і d не були передані через POST, виводимо повідомлення про помилку
    echo "<h2>Помилка: Введіть значення для змінних c і d через форму.</h2>";
}
